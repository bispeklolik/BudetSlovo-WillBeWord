// Библиотека промтов — ядро рефрейма «разговор → любой нужный результат».
// Каждый пресет = имя + system-промт, который прогоняется по расшифровке
// (текст «Говорящий: реплика»). Один ИИ-движок (локальный/Claude), много
// промтов. Пользователь может добавить свои (CustomPrompt, хранятся в настройках).

export type PromptCategory =
  | 'universal'
  | 'psychology'
  | 'meeting'
  | 'legal'
  | 'medical'
  | 'teaching'
  | 'journalism'
  | 'sales'

export interface PromptPreset {
  id: string
  name: string
  category: PromptCategory
  system: string
}

// Пользовательский промт (создаётся в приложении, хранится в settings.customPrompts).
export interface CustomPrompt {
  id: string
  name: string
  system: string
}

